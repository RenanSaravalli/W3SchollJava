package introduction.metodos;

public class JvEscopo {
    public static void main(String[] args) {
        // Em Java, variáveis são acessíveis somente dentro da região em que são criadas.
        // Isso é chamado de escopo

        // Âmbito do métod
        // Variáveis declaradas diretamente dentro de um métod estão disponíveis em
        // qualquer lugar do métd após a linha de código na qual foram declaradas.

        // Escopo do bloco
        // um bloco de código se refere a td código entre chaves {}, que segue a linha
        // na qual a variável foi declarada

        // Um bloco de código pode existir por si só ou pode pertencer a uma instrução
        // if, while ou for. No caso de for instruções, as variáveis declaradas na própria
        // instrução tb estão disponíveis dentro do escopo do bloco.
    }
}
