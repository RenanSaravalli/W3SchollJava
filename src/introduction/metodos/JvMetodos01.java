package introduction.metodos;

public class JvMetodos01 {
    public static void main(String[] args) {
        /* Um métod deve ser declarado dentro de uma classe. */

        // Chamando um métod
        myMethod();
    }

    static void myMethod() {
        // Code to be executed
        System.out.println("O método foi executado!!");
    }
    // static significa que o métod pertence à classe Main e não a um obj da classe JvMetodos01

}
